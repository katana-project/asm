/** Annotation element value tag. (https://docs.oracle.com/javase/specs/jvms/se25/html/jvms-4.html#jvms-4.7.16.1-130) */
export enum ElementTag {
    BYTE = "B",
    CHAR = "C",
    DOUBLE = "D",
    FLOAT = "F",
    INT = "I",
    LONG = "J",
    SHORT = "S",
    BOOLEAN = "Z",
    STRING = "s",
    ENUM = "e",
    CLASS = "c",
    ANNOTATION = "@",
    ARRAY = "[",
}
